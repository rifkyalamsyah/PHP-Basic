<?php

// String Function

// example

// join
var_dump(join(",", [10, 11, 12, 13, 14, 15]));
// explode
var_dump(explode(" ", "Rifky Alamsyah"));
// strtolower
var_dump(strtolower("RIFKY ALAMSYAH"));
// strtoupper
var_dump(strtoupper("rifky alamsyah"));
// trim
var_dump(trim("     Rifky   Alamsyah       "));
// substr
var_dump(substr("Rifky Alamsyah", 0, 10));