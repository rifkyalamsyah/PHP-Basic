<?php

// Is Function

$data = "Rifky";

// untuk mnegecek string
var_dump(is_string($data));
// untuk mnegecek boolean
var_dump(is_bool($data));
// untuk mnegecek integer
var_dump(is_int($data));
// untuk mnegecek float
var_dump(is_float($data));
// untuk mnegecek array
var_dump(is_array($data));
// untuk mnegecek null
var_dump(is_null($data));
