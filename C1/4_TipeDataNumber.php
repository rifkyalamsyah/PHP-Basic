<?php

// tipe data number
echo "decimal : ";
var_dump(1234);

echo "octal : ";
var_dump(01234);

echo "hexadecimal : ";
var_dump(0x1A);

echo "binary : ";
var_dump(0b111111);

echo "underscore in number : ";
var_dump(1_234_567_789);



// tipe data float / floating point
echo "floating point";
var_dump(1.234);

echo "floating point dengan E notation : 1.7 x 1000) :";
var_dump(1.7e3);

echo "floating point dengan E notation minus : (1.7 x 0.001) :";
var_dump(1.7e-3);

echo "floating point dengan E notation";
var_dump(1.234);

echo "underscore in floating point : ";
var_dump(1_234.123);
