<?php

// Operator Penugasam Aritmatika

// Example.
/*
Penugasan    =        Keterangan
$a += $b     =       $a = $a + $b
$a -= $b     =       $a = $a - $b
$a *= $b     =       $a = $a * $b
$a /= $b     =       $a = $a / $b
$a %+= $b     =       $a = $a % $b
*/


$total = 0;

$fruite = 5000;
$chicken = 10000;
$orangeJuice = 6000;

$total += $fruite;
$total += $chicken;
$total += $orangeJuice;

var_dump($total);